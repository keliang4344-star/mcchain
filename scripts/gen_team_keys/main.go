package main

import (
	"encoding/json"
	"fmt"
	"os"
	"sort"
	"time"

	bip39 "github.com/cosmos/go-bip39"
	"github.com/cosmos/cosmos-sdk/codec"
	"github.com/cosmos/cosmos-sdk/crypto/hd"
	"github.com/cosmos/cosmos-sdk/crypto/keys/multisig"
	"github.com/cosmos/cosmos-sdk/crypto/keys/secp256k1"
	cryptotypes "github.com/cosmos/cosmos-sdk/crypto/types"
	sdk "github.com/cosmos/cosmos-sdk/types"
	"github.com/cosmos/cosmos-sdk/types/bech32"
)

// KeyOut 单条团队密钥的导出形态（team_keys_gen.json）。私钥由 bip39 助记词恢复。
type KeyOut struct {
	Name     string `json:"name"`
	Address  string `json:"address"`
	PubB32   string `json:"pubkey_mcpub"`
	Mnemonic string `json:"mnemonic"`
}

const (
	jsonPath  = "$HOME/mcchain/team_keys_gen.json"
	goPath    = "$HOME/mcchain/x/tokenomics/types/team_pubkeys_gen.go"
	vaultPath = "$HOME/mcchain/team_multisig_vault.txt"
)

func main() {
	cfg := sdk.GetConfig()
	cfg.SetBech32PrefixForAccount("mc", "mcpub")
	cfg.SetBech32PrefixForValidator("mcvaloper", "mcvaloperpub")
	cfg.SetBech32PrefixForConsensusNode("mcvalcons", "mcvalconspub")

	// 1) 读取现有助记词（若存在）作为规范种子；否则生成 5 个随机助记词。
	mnemonics := readOrGenerateMnemonics()

	// 2) 用与 mcchaind keyring 完全一致的方式派生 5 个公钥。
	out := make([]KeyOut, 0, 5)
	pubs := make([]cryptotypes.PubKey, 0, 5)
	for i := 1; i <= 5; i++ {
		mn := mnemonics[i-1]
		pub := keyringDerive(mn)
		pubs = append(pubs, pub)
		out = append(out, KeyOut{
			Name:     fmt.Sprintf("team%d", i),
			Address:  fmt.Sprintf("%X", pub.Address()),
			PubB32:   aminoMcpub(pub),
			Mnemonic: mn,
		})
		fmt.Printf("team%d mcpub=%s\n", i, out[i-1].PubB32)
	}

	// 3) 重建 3-of-5 多签（按地址排序，与 mcchaind `keys add --multisig` 默认行为一致），
	//    打印地址用于核对。必须与链上 buildTeamPubKeys 的排序方式相同。
	sort.Slice(pubs, func(i, j int) bool {
		return string(pubs[i].Address().Bytes()) < string(pubs[j].Address().Bytes())
	})
	ms := multisig.NewLegacyAminoPubKey(3, pubs)
	teamAddr := sdk.AccAddress(ms.Address()).String()
	fmt.Printf("TeamAddress (3-of-5, sorted) = %s\n", teamAddr)

	// 4) 写出三处产物，保证完全同步。
	writeJSON(out)
	writeGoFile(out)
	writeVault(out, teamAddr)

	fmt.Println("✅ 已同步生成 team_pubkeys_gen.go / team_keys_gen.json / team_multisig_vault.txt")
}

// readOrGenerateMnemonics 若 team_keys_gen.json 已有助记词则复用，保证幂等（不漂移）；
// 否则生成 5 个全新 256-bit 熵助记词。
func readOrGenerateMnemonics() []string {
	if b, err := os.ReadFile(jsonPath); err == nil {
		var existing []KeyOut
		if json.Unmarshal(b, &existing) == nil && len(existing) == 5 {
			mn := make([]string, 0, 5)
			for _, e := range existing {
				if e.Mnemonic == "" {
					return generateMnemonics()
				}
				mn = append(mn, e.Mnemonic)
			}
			fmt.Println("复用 team_keys_gen.json 现有助记词（幂等，保持链编译多签不变）")
			return mn
		}
	}
	return generateMnemonics()
}

func generateMnemonics() []string {
	mn := make([]string, 0, 5)
	for i := 0; i < 5; i++ {
		entropy, err := bip39.NewEntropy(256)
		if err != nil {
			panic(err)
		}
		m, err := bip39.NewMnemonic(entropy)
		if err != nil {
			panic(err)
		}
		mn = append(mn, m)
	}
	fmt.Println("生成 5 个全新助记词")
	return mn
}

// BIP44_PATH 必须与 mcchaind keyring 实际使用的派生路径完全一致（经验证为
// m/44'/118'/0'/0/0）。注意：sdk.GetConfig().GetFullBIP44Path() 的默认值在本仓库
// 独立 go 程序里并非 118（会导致生成的公钥与 `mcchaind keys add --recover` 不一致、
// 团队资金永久锁定）。因此这里显式写死该路径。
const BIP44_PATH = "m/44'/118'/0'/0/0"

// keyringDerive 复刻 mcchaind keyring 的派生（bip39 标准路径 m/44'/118'/0'/0/0），
// 保证团队用 `mcchaind keys add --recover` 能恢复出同一密钥。
func keyringDerive(mnemonic string) cryptotypes.PubKey {
	seed, err := bip39.NewSeedWithErrorChecking(mnemonic, "")
	if err != nil {
		panic(fmt.Sprintf("invalid mnemonic: %v", err))
	}
	masterPriv, chainCode := hd.ComputeMastersFromSeed(seed)
	derivedPriv, err := hd.DerivePrivateKeyForPath(masterPriv, chainCode, BIP44_PATH)
	if err != nil {
		panic(err)
	}
	priv := &secp256k1.PrivKey{Key: derivedPriv}
	return priv.PubKey()
}

// aminoMcpub 把 secp256k1 公钥做 amino 编码后转 bech32 mcpub（与链一致）。
func aminoMcpub(pub cryptotypes.PubKey) string {
	cdc := codec.NewLegacyAmino()
	cdc.RegisterInterface((*cryptotypes.PubKey)(nil), nil)
	cdc.RegisterConcrete(&secp256k1.PubKey{}, "tendermint/PubKeySecp256k1", nil)
	aminoBz, err := cdc.Marshal(pub)
	if err != nil {
		panic(err)
	}
	mcpub, err := bech32.ConvertAndEncode("mcpub", aminoBz)
	if err != nil {
		panic(err)
	}
	return mcpub
}

func writeJSON(out []KeyOut) {
	b, err := json.MarshalIndent(out, "", "  ")
	if err != nil {
		panic(err)
	}
	if err := os.WriteFile(jsonPath, append(b, '\n'), 0600); err != nil {
		panic(err)
	}
	fmt.Println("WROTE", jsonPath)
}

func writeGoFile(out []KeyOut) {
	src := "package types\n\n"
	src += "// Code generated by scripts/gen_team_keys. DO NOT EDIT.\n"
	src += "// 与 team_multisig_vault.txt / team_keys_gen.json 保持同步；私钥由各自 bip39 助记词恢复。\n"
	src += "// teamPubKeyStrings 真实团队 3-of-5 多签公钥（bech32 mcpub 前缀，共 5 个）。\n"
	src += "var teamPubKeyStrings = []string{\n"
	for _, o := range out {
		src += fmt.Sprintf("\t%q,\n", o.PubB32)
	}
	src += "}\n"
	if err := os.WriteFile(goPath, []byte(src), 0644); err != nil {
		panic(err)
	}
	fmt.Println("WROTE", goPath)
}

func writeVault(out []KeyOut, teamAddr string) {
	v := "================================================================\n"
	v += "MC 公链 · 团队 3-of-5 多签密钥密码本（绝密 · 仅本地保管）\n"
	v += "================================================================\n"
	v += fmt.Sprintf("生成时间: %s\n", nowStr())
	v += "生成工具: scripts/gen_team_keys（cosmos-sdk v0.47.3 标准 bip39 + hd 路径 m/44'/118'/0'/0/0）\n"
	v += "用途:     这 5 个公钥已写入 x/tokenomics/types/team_pubkeys_gen.go 的 teamPubKeyStrings，\n"
	v += "          构成主网团队池（1.5e14 umc = 1.5e8 MC）的 3-of-5 多签 vesting 账户。\n"
	v += "          任意 3 个助记词即可动用团队资金，务必分散保管！\n"
	v += fmt.Sprintf("团队多签地址 TeamAddress: %s\n\n", teamAddr)
	for _, o := range out {
		v += fmt.Sprintf("------------------------------ %s ------------------------------\n", o.Name)
		v += fmt.Sprintf("地址(hex) : %s\n", o.Address)
		v += fmt.Sprintf("公钥(mcpub): %s\n", o.PubB32)
		v += fmt.Sprintf("助记词    : %s\n\n", o.Mnemonic)
	}
	v += "================================================================\n"
	v += "恢复方法（任一私钥）:\n"
	v += "  mcchaind keys add teamX --recover --keyring-backend <backend>\n"
	v += "  然后粘贴对应助记词即可恢复该密钥对。\n"
	v += "================================================================\n"
	if err := os.WriteFile(vaultPath, []byte(v), 0600); err != nil {
		panic(err)
	}
	fmt.Println("WROTE", vaultPath)
}

func nowStr() string {
	return time.Now().Format("2006-01-02 15:04:05")
}
